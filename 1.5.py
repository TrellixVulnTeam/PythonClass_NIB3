#1.5计算圆柱体体积和表面积
r = eval(input("输入圆柱体的半径："))
h = eval(input("输入圆柱体的高："))
V = 3.1415926*r**2*h
print(f"圆柱体的体积为：{V}")
S = 2*3.1415926*r**2+2*3.1415926*r*h
print(f"圆柱体的表面积为：{S}")

#1.6计算长方体体积
a=input("你想计算的长方体的长为：")
b=input("你想计算的长方体的宽为：")
